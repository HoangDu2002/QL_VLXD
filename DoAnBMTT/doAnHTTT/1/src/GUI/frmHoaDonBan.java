    public void loadDataToTable() {
        HoaDonBanDAO p = new HoaDonBanDAO();
        ArrayList<HoaDonBanPOJO> ds = p.dsHD();

        DefaultTableModel model = (DefaultTableModel) tbl_qlhd.getModel();
        model.setRowCount(0);
        model.setColumnIdentifiers(new Object[]{"Mã hoá đơn", "Mã nhân viên", "Mã mặt hàng", "Tên mặt hàng", "Số lượng", "Đơn giá", "Tổng tiền", "Ngày bán"});
        for (HoaDonBanPOJO hd : ds) {
            Object[] row = new Object[]{hd.mahd, hd.manv, hd.mamh, hd.tenmh, hd.soluong, hd.dongia, hd.tongtien, hd.ngayban};
            model.addRow(row);
        }
    }


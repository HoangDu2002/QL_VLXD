    public void bidingtblPB() {
        HoaDonBanDAO p = new HoaDonBanDAO();
        ArrayList<HoaDonBanPOJO> ds = p.dsHD();
        int index = tbl_qlhd.getSelectedRow();
        HoaDonBanPOJO hd = ds.get(index);
        txtMahd.setText(hd.getMahd());
        this.txtMahd.setEditable(false);
        txtManv.setText(hd.getManv());
        this.txtManv.setEditable(false);
        txtMamh.setText(hd.getMamh());
        this.txtMamh.setEditable(false);
        txtTenmh.setText(hd.getTenmh());
        txtSoluong.setText(hd.getSoluong() + "");
        txtDongia.setText(hd.getDongia() + "");
        txtTongtien.setText(hd.getTongtien() + "");
        txtNgayban.setText(hd.getNgayban());
    }

